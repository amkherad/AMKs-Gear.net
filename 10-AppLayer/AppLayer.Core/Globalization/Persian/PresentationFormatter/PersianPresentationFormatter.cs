            return number.ToString();
        }
        public static string ToPresentationString(decimal number)
        {
            return number.ToString();
        }
        public static string ToPresentationString(long number)
        {
            return number.ToString();
        }
        public static string ToPresentationString(int number)
        {
            return number.ToString();
        }
    }
}